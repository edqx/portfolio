    <h1 class="text-5xl">SwagClan</h1>
    <span class="text-1xl text-slate-600">2019-2020</span>
    <p>
        Along with <a href="/p/ryuujin">Ryuujin</a>, SwagClan was one of my more humble projects, being a small discord bot in a couple
        of servers that I invited it to. While ultimately humble, it did have an ambitious goal and even had a few features that you
        can only find in some of the biggest and often paid server bots.
        <br><br>
        For example, it had a fully workable block-based custom command creator, allowing users to develop their own commands with access to
        a full collection-based database. Securing a system like that was a good challenge, as well as good experience.
        <br><br>
        SwagClan was just one of the first of many discord bots I've worked on in the past. Future ones had full scalability with
        <a href="https://redis.io">Redis</a> pub/subs and access to both <a href="https://www.mongodb.com/">MongoDB</a> and
        <a href="https://www.postgresql.org/">Postgres</a>.
        <br><br>
        All versions of SwagClan had fully interactive and designed web dashboards, which interacted with a public HTTP REST API written
        using <a href="https://expressjs.com">Express</a>.
