    <h1 class="text-5xl">AUProximity</h1>
    <span class="text-1xl text-slate-600">2021-2022</span>

    <p>
        AUProximity was a web-based, unintrusive mod for Among Us that allowed for voice communication with allowing for only being able to hear
        people within close proximity, and weren't obstructed by walls.
        <br><br>
        I took over development for AUProximity after the previous owner stopped maintaining the project. I worked on it with another
        developer (going by "miniduikboot"), who gave me insight into using <a href="https://grafana.com/">Grafana</a>,
        <a href="https://prometheus.io/">Prometheus</a>, <a href="https://glitchtip.com/">Glitchtip</a> and <a href="https://www.hetzner.com/">Hetzner</a>
        cloud servers.
        <br><br>
        Taking over development, I was tasked with replacing the older, broken code with newer code from my <a href="/p/skeldjs">SkeldJS</a>
        libary. AUProximity was actively developed after each Among us update, and had over 10,000 users on its discord server at its peak,
        with hundreds of active users at once.
        <br><br>
        AUProximity gave me experience into <a href="https://webrtc.org/">WebRTC</a> and voice communication on the web, as well as developing
        a good project architecture to fit the requirements of hundreds of users and several lobbies open at the same time.
        <br><br>
