    <h2 class="text-3xl">TypeScript</h2>
    <p>
        I was introduced to TypeScript through the youtuber <a href="https://www.youtube.com/watch?v=ahCwqrYpIuM">Fireship</a>, and it completely transformed
        the way I wrote code both on the server and on the web.
        <br><br>
        Most of my more recent JavaScript projects are written with TypeScript, so that users and I can be confident that bugs that could've
        been caught during development never affect usage in production.
        <br><br>
        I've been using TypeScript to its full extent, with the use of experimental <a href="https://www.typescriptlang.org/docs/handbook/decorators.html">decorators</a>
        used throughout in <a href="/p/skeldjs#hb">Hindenburg</a>, as well as the <a href="https://www.npmjs.com/package/reflect-metadata">Reflection metadata API</a>.
        Additionally, <a href="/p/skeldjs">SkeldJS</a> uses the great type system to allow for easy extensibility.
    </p>
